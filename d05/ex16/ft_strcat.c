char	*ft_strcat(char *dest, char *src)
{
	int len;
	int i;

	len = -1;
	i = -1;
	while (*(dest + ++len))
	{
	}
	while (*(src + ++i))
		dest[len + i] = *(src + i);
	dest[len + i] = '\0';
	return (dest);
}
